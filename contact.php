    // Copy to clipboard function
    window.copyToClipboard = function(text, element) {
      console.log('Copying text:', text); // Debug log
      
      // Check if clipboard API is available
      if (navigator.clipboard && window.isSecureContext) {
        navigator.clipboard.writeText(text).then(function() {
          console.log('Copied successfully via clipboard API'); // Debug log
          showCopyNotification(element);
        }).catch(function(err) {
          console.log('Clipboard API failed, using fallback:', err); // Debug log
          fallbackCopy(text, element);
        });
      } else {
        console.log('Using fallback copy method'); // Debug log
        fallbackCopy(text, element);
      }
    };

    function fallbackCopy(text, element) {
      const textArea = document.createElement('textarea');
      textArea.value = text;
      textArea.style.position = 'fixed';
      textArea.style.left = '-999999px';
      textArea.style.top = '-999999px';
      document.body.appendChild(textArea);
      textArea.focus();
      textArea.select();
      
      try {
        const successful = document.execCommand('copy');
        console.log('Fallback copy successful:', successful); // Debug log
        showCopyNotification(element);
      } catch (err) {
        console.error('Fallback copy failed:', err);
      }
      
      document.body.removeChild(textArea);
    }

    function showCopyNotification(element) {
      const notification = element.querySelector('.copy-notification');
      if (notification) {
        notification.classList.add('show');
        setTimeout(() => {
          notification.classList.remove('show');
        }, 2000);
      }
    }
