    <p>
      Lighting is truly in <span class="highlight">Kerie Boshka's DNA</span>. A third-generation lighting designer, she proudly carries on her family's legacy that began more than 60 years ago when her grandfather founded C&M Supply in Lubbock, Texas. Her grandmother later encouraged her parents to expand into lighting, and Dement Lighting was born—now owned and operated by her brother and sister-in-law. Today, Kerie continues that legacy through Boshka Lighting & Electric, a sister company that brings her full circle to work with family once again.
    </p>
    <p>
      A <span class="highlight">1993 graduate of Aledo High School</span>, Kerie grew up working at her family's Dement True Value Hardware store, where she developed a lifelong love of small-town community and craftsmanship. At 29, she moved to the New York/Connecticut area, where she and her husband owned Boshka Electric and Boshka Interiors. Together, they built a strong design-and-electrical team—Kerie leading the lighting design side and Rick serving as the Master Electrician, employing multiple crews who worked on residential remodels and new construction projects. Working with clients in the Northeast helped Kerie master the art of <span class="highlight">small-space design, remodels, and lighting layouts</span> from concept to completion.
    </p>
    <p>
      A self-described <span class="highlight">lighting nerd</span>, Kerie can walk into any room and instantly identify the fixtures, style, and craftsmanship. She's passionate about helping homeowners find their perfect look—beautiful, personal, and always on budget.
    </p>
    <p>
      Kerie is also a <span class="highlight">published author</span> of <em>If God Cares So Much, Why Do I Still Hurt?</em> and the children's book <em>Toad, Frog, Mouse, and Monkey Boy: Magic and Mayhem</em>. She is the founder of Fantastic Life Choices, a program that helps kids make positive choices by helping others, and the co-founder of <span class="highlight">BuildHERS – Building Up Women Who Build</span>, an organization empowering women across the construction and design industries.
    </p>
    <p>
      For Kerie, a beautiful home is where peace begins—and her greatest joy is helping others create spaces filled with warmth, light, and meaning.
